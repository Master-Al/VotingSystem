    private void tblCandidatesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblCandidatesMouseClicked
        try {
            // TODO add your handling code here:
            String name = tblCandidates.getValueAt(tblCandidates.getSelectedRow(), 0).toString();
            String div = tblCandidates.getValueAt(tblCandidates.getSelectedRow(), 1).toString();
            String pos = tblCandidates.getValueAt(tblCandidates.getSelectedRow(), 2).toString();

            txtCandidate.setText(name);
            cmbPos.setSelectedItem(pos);
            cmbDiv.setSelectedItem(div);

            Connection c = DatabaseConnection.getDatabaseConnection();
            PreparedStatement ps = c.prepareStatement("SELECT * FROM candidates WHERE CANDIDATENAME = ? AND DIVISION=? AND POSITION=?");
            ps.setString(1, txtCandidate.getText().trim());
            ps.setString(2, cmbDiv.getSelectedItem().toString().trim());
            ps.setString(3, cmbPos.getSelectedItem().toString().trim());
            ResultSet results = ps.executeQuery();
            results.beforeFirst();
            if (results.next()) {
                id = results.getInt("ID");
            }

            isEdit = true;
        } catch (SQLException ex) {
            Logger.getLogger(AdminInterface.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_tblCandidatesMouseClicked

