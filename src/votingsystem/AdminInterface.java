    private void BSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BSubmitActionPerformed
        // TODO add your handling code here:
        //verify admin
        adminPass = false;
        diagAdminPass.pack();
        diagAdminPass.setLocationRelativeTo(this);
        diagAdminPass.setVisible(true);

        if (!adminPass) {
            JOptionPane.showMessageDialog(this, "Please enter correct authorization code");
            return;
        }

        //check fields
        if (txtCandidate.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter candidate name");
            return;
        }

        if (cmbDiv.getSelectedIndex() == -1) {
            JOptionPane.showMessageDialog(this, "Please Select proper division");
            return;
        }
        if (cmbPos.getSelectedIndex() == -1) {
            JOptionPane.showMessageDialog(this, "Please Select proper position");
            return;
        }

        Connection c = DatabaseConnection.getDatabaseConnection();
        //check record
        try {
            PreparedStatement ps = c.prepareStatement("SELECT * FROM candidates WHERE CANDIDATENAME = ? AND DIVISION=? AND POSITION=?");
            ps.setString(1, txtCandidate.getText().trim());
            ps.setString(2, cmbDiv.getSelectedItem().toString().trim());
            ps.setString(3, cmbPos.getSelectedItem().toString().trim());
            ResultSet results = ps.executeQuery();
            results.beforeFirst();
            if (results.next()) {
                JOptionPane.showMessageDialog(this, "Duplicate record found!");
                return;
            }

            if (isEdit) {
                System.out.println(id);
                PreparedStatement pEdit = c.prepareStatement("UPDATE candidates SET CANDIDATENAME = ?, DIVISION= ?, POSITION=? WHERE ID = ?");
                pEdit.setString(1, txtCandidate.getText().trim());
                pEdit.setString(2, cmbDiv.getSelectedItem().toString().trim());
                pEdit.setString(3, cmbPos.getSelectedItem().toString().trim());
                pEdit.setInt(4, id);
                pEdit.executeUpdate();
                JOptionPane.showMessageDialog(this, "Candidate information updated.");
                isEdit = false;
            } else {
                ps = c.prepareStatement("INSERT INTO candidates(CANDIDATENAME, DIVISION, POSITION) VALUES (?,?,?)");
                ps.setString(1, txtCandidate.getText().trim());
                ps.setString(2, cmbDiv.getSelectedItem().toString().trim());
                ps.setString(3, cmbPos.getSelectedItem().toString().trim());
                ps.executeUpdate();
                JOptionPane.showMessageDialog(this, "Candidate successfully added!");
            }

            txtCandidate.setText("");
            cmbDiv.setSelectedIndex(-1);
            cmbPos.setSelectedIndex(-1);
            txtAdminPass.setText("");

        } catch (Exception ex) {
            Logger.getLogger(AdminInterface.class.getName()).log(Level.SEVERE, null, ex);
        }
        txtAdminPass.setText("");

        refreshTable();
    }//GEN-LAST:event_BSubmitActionPerformed

